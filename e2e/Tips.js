// Meaning of Your Commands
// 1. npx playwright codegen www.gmail.com
// Purpose:
// • This command opens Playwright Codegen, which records your UI actions and automatically generates test code.
// • If you click on a date picker, it will generate the exact code needed to select a date.
// So this is used for creating the code for your date‑picker test case.

// 2. npx playwright ui
// Purpose:
// • This starts the Playwright Test UI Mode.
// • It allows you to run tests visually, debug step‑by‑step, view locators, recordings, etc.
// • Whatever steps you perform or re-run will be shown interactively.
// So this is used for executing and debugging your test using a UI.



